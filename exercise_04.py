# 1. Define the function to convert Celsius to Fahrenheit
def celsius_to_fahrenheit(celsius):
    fahrenheit = (9/5) * celsius + 32
    return fahrenheit

# 2. Call the function for celsius = 25 and celsius = 10 and print the results
print("25°C in Fahrenheit:", celsius_to_fahrenheit(25))
print("10°C in Fahrenheit:", celsius_to_fahrenheit(10))